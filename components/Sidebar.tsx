interface SidebarProps {
  userRole?: string;
  pendingCount?: number;
}

function isAdmin(role: string) {
  return role === "super_admin" || role === "admin";
}

export default function Sidebar({ userRole = "", pendingCount = 0 }: SidebarProps) {
  const pathname = usePathname();

  const navItems = baseNavItems
    .filter(item => !item.adminOnly || isAdmin(userRole))
    .map(item =>
      item.href === "/approvals" && pendingCount > 0
        ? { ...item, badge: pendingCount }
        : item
    );

  return (
    <aside className="fixed left-0 top-0 h-full w-60 bg-white border-r border-purple-100 flex flex-col z-30">
      {/* Logo */}
      <div className="flex items-center gap-3 px-6 py-5 border-b border-purple-100">
        <div className="w-9 h-9 rounded-xl bg-brand flex items-center justify-center flex-shrink-0">
          <svg className="w-5 h-5 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2}
              d="M3.055 11H5a2 2 0 012 2v1a2 2 0 002 2 2 2 0 012 2v2.945M8 3.935V5.5A2.5 2.5 0 0010.5 8h.5a2 2 0 012 2 2 2 0 104 0 2 2 0 012-2h1.064M15 20.488V18a2 2 0 012-2h3.064" />
          </svg>
        </div>
        <div>
          <p className="text-sm font-bold text-text-dark leading-tight">BOC Sports</p>
          <p className="text-xs text-text-grey leading-tight">Society</p>
        </div>
      </div>

      {/* Nav */}
      <nav className="flex-1 px-3 py-4 space-y-1 overflow-y-auto">
        {navItems.map((item) => {
          const isActive = pathname === item.href || (item.href !== "/events" && pathname.startsWith(item.href + "/"));
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium transition-colors ${
                isActive
                  ? "bg-brand text-white shadow-sm"
                  : "text-text-grey hover:bg-brand-bg hover:text-brand-dark"
              }`}
            >
              {item.icon}
              <span className="flex-1">{item.label}</span>
              {item.badge != null && item.badge > 0 && (
                <span className={`inline-flex items-center justify-center min-w-[20px] h-5 px-1.5 rounded-full text-xs font-bold ${isActive ? "bg-white/25 text-white" : "bg-amber-100 text-amber-700"}`}>
                  {item.badge > 99 ? "99+" : item.badge}
                </span>
              )}
            </Link>
          );
        })}
      </nav>

      {/* Footer */}
      <div className="px-4 py-4 border-t border-purple-100">
        <p className="text-xs text-text-grey text-center">Bank of Ceylon Sports Society</p>
      </div>
    </aside>
  );
}
